/**
 * Loads the shared library containing JNI based methods used by subclasses.
 * 
 * @see NativeProcesss
 * @see NativeSystem
 */
class Native
{  
  /*----------------------------------------------------------------------------------------*/
  /*   C O N S T R U C T O R                                                                */
  /*----------------------------------------------------------------------------------------*/

  protected
  Native()
  {}



  /*----------------------------------------------------------------------------------------*/
  /*   S T A T I C   I N I T I A L I Z A T I O N                                            */
  /*----------------------------------------------------------------------------------------*/
  
  static {
    System.load(PackageInfo.sInstDir + "/lib/libNative.so");
  }
}
